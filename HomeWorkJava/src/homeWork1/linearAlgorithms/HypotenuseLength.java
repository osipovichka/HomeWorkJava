package homeWork1.linearAlgorithms;

public class HypotenuseLength {
//    Найти длину гипотенузы (по двум введенным пользователем катетам
//            вычислить длину гипотенузы.)

    public double GetHypotenuseLength(int cat1, int cat2){
        double a = Math.sqrt(Math.pow(cat1, 2) + Math.pow(cat1, 2));
        return a;
    }
}
